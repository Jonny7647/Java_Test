package day01;

public class Test02 {
	public static void main(String[] args) {
		
		//변수선언
		boolean b;
		char ch;
		int i;
		double d;
		
		b = true;
		ch = 'A';
		i = 100;
		d = 3.14;
		
		System.out.println(b);
		System.out.println(ch);
		System.out.println(i);
		System.out.println(d);
		
		
		
		//선언 후 대입
		int abc = 10;
		int def = 20;
		
		System.out.println(abc);
		System.out.println(def);
		
		
		
		//대입연산자
		abc = 10 + 20;
		System.out.println(abc);
		
		
		
	} //main

} //class
